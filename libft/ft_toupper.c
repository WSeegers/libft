#include "libft.h"

int		ft_toupper(int c)
{
	if (c == FT_EOF)
		return (FT_EOF);
	if (c >= -128 && c < 256)
	{
		c = c & 0xff;
		if (ft_islower(c))
			return (c & ~0x20);
	}
	return (c);
}
