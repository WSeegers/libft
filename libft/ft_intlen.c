#include <string.h>

size_t	ft_intlen(int nbr)
{	
	size_t len;

	if (nbr < 0)
		nbr *= -1;
	if (nbr == 0)
		return (1);
	len = 0;
	while (nbr > 0)
	{
		len++;
		nbr /= 10;
	}
	return (len);
}
