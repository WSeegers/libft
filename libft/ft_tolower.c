#include "libft.h"

int		ft_tolower(int c)
{
	if (c == EOF)
		return (EOF);
	if (c >= -128 && c < 256)
	{
		c = c & 0xff;
		if (ft_isupper(c))
			return (c | 0x20);
	}
	return (c);
}
